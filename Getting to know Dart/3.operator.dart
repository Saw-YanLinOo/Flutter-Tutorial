void main() {
  //Arithmatic operator
  print(43 + 5);
  print(43 - 5);
  print(43 * 5);
  print(43 / 5);

  //Equality operator
  print(44 == 6);
  print(6 != 7);

  //Increment and decrement Operator
  var number = 2;
  print(++number);
  print(number++);

  var anotherNum = 4;
  print(--anotherNum);
  print(anotherNum--);

  //Compound Assignment Operator
  var firstNum = 5;
  firstNum += 1;
  print(firstNum);

  var secondNum = 6;
  secondNum -= 2;
  print(secondNum);

  //Comparison Operator
  print(55 < 50);
  print(55 <= 50);
  print(55 << 50);
  print(55 > 50);
  print(55 <= 50);
  print(55 >> 50);

  //Logical Operator
  print(44 < 25 && 34 < 32);
  print(44 > 25 || 34 < 32);

  //Negation Operator
  print(!(25 > 60));
  
}
